package com.jason.spider.rule;

public class Rule {
	
	private String encoding;
	
	private String contentTag;
	
	private String contentTagId;
	
	private String contentTagClass;

	public String getContentTag() {
		return contentTag;
	}

	public void setContentTag(String contentTag) {
		this.contentTag = contentTag;
	}

	public String getContentTagId() {
		return contentTagId;
	}

	public void setContentTagId(String contentTagId) {
		this.contentTagId = contentTagId;
	}

	public String getContentTagClass() {
		return contentTagClass;
	}

	public void setContentTagClass(String contentTagClass) {
		this.contentTagClass = contentTagClass;
	}

	public String getEncoding() {
		return encoding;
	}

	public void setEncoding(String encoding) {
		this.encoding = encoding;
	}
	
	
	

}
